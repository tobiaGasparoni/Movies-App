package com.example.mr_kotlin.data.genre

class GenrePieChart(

    /***********************************************************************************************
     * Attribute that contains the genre id
     **********************************************************************************************/
    private var genreId: Int,

    /***********************************************************************************************
     * Attribute that tahta has the amount of likes that the user has
     **********************************************************************************************/
    private var amountLikes: Int,

    /***********************************************************************************************
     * Attribute that represents the genre of the name
     **********************************************************************************************/
    private var genreName: String,

    /***********************************************************************************************
     * Attribute that represents user email of the genre
     **********************************************************************************************/
    private var userEmail: String

) {

    /***********************************************************************************************
     * @return the genre id
     **********************************************************************************************/
    fun getgenreId(): Int {
        return genreId
    }

    /***********************************************************************************************
     * @return the user's email
     **********************************************************************************************/
    fun getUserEmail(): String {
        return userEmail
    }

    /***********************************************************************************************
     * @return the amount of likes of the movie
     **********************************************************************************************/
    fun getamountLikes(): Int {
        return amountLikes
    }


    /***********************************************************************************************
     * @return the Genre name
     **********************************************************************************************/
    fun getgenreName(): String {
        return genreName
    }

}
